#!/usr/bin/env python3
"""A python script to do some computational calculations"""
digit1 = input("Enter the first digit\n")
digit2 = input("Enter the second digit\n")
a = int(digit1)
b = int(digit2)

print("The addition of a and b is: {}".format(a + b))
print("The subtraction of a and b is: {}".format(a - b))
print("The division of a and b is: {}".format(a / b))
print("The multipliction of a and b is: {}".format(a * b))
print("The mod of a and b is: {}".format(a % b))
